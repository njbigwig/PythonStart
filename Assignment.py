x, y, z = 10, 5, 10

print("Current value of x is: ", x)
print("Current value of y is: ", y)
print("Current value of z is: ", z)

x += y
print("Current value of x after performing x += y: ", x)

x -= z
print("Current value of x after performing x -= z: ", x)

y *= z
print("Current value of y after performing y *= z: ", y)

y /= x
print("Current value of y after performing y /= x: ", y)

x **= y
print("Current value of x after performing x** = y: ", x)

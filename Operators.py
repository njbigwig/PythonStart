num1 = int(input("Enter the first number: "))
num2 = int(input("Enter the second number: "))

print("The return value after addition operation is: ", num1 + num2)

print("The return value after subtraction operation is: ", num1 - num2)

print("The return value after multiplication operation is: ", num1 * num2)

print("The return value after division operation is: ", num1 / num2)
     
print("The return value after floor division operation is: ", num1 // num2) 

print("The return value after modulus operator is: ", num1 % num2) 

print("The return value after exponential operator is: ", num1 ** num2) 